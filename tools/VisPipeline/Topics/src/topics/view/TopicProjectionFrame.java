        class MouseMotionListener extends MouseMotionAdapter {

            @Override
            public void mouseMoved(java.awt.event.MouseEvent evt) {
                super.mouseMoved(evt);

                if (model != null) {
                    if (highlighttopic || evt.isControlDown()) {
                        for (Topic t : ((TopicProjectionModel) model).getTopics()) {
                            t.setShowTopic(false);
                        }

                        Topic topic = ((TopicProjectionModel) model).getTopicByPosition(evt.getPoint());

                        if (topic != null) {
                            topic.setShowTopic(true);
                        }

                        model.notifyObservers(Boolean.FALSE);
                    } else {
                        ProjectionInstance instance = ((TopicProjectionModel) model).getInstanceByPosition(evt.getPoint());

                        if (instance != null) {
                            //Show the instance label
                            label = instance.toString();

                            if (label != null && label.trim().length() > 0) {
                                if (label.length() > 100) {
                                    label = label.substring(0, 96) + "...";
                                }

                                labelpos = evt.getPoint();
                                repaint();
                            }
                        } else {
                            //Clear the label
                            label = null;
                            labelpos = null;
                            repaint();
                        }
                    }
                }
            }

            @Override
            public void mouseDragged(java.awt.event.MouseEvent evt) {
                if (selinstance != null) {
                    if (model.hasSelectedInstances()) {
                        float x = evt.getX() - selinstance.getX();
                        float y = evt.getY() - selinstance.getY();

                        for (AbstractInstance ai : model.getSelectedInstances()) {
                            ProjectionInstance pi = (ProjectionInstance) ai;
                            pi.setX(x + pi.getX());
                            pi.setY(y + pi.getY());
                        }

                        adjustPanel();
                    }
                } else if (selsource != null) {
                    seltarget = evt.getPoint();
                } else if (selpolygon != null) {
                    selpolygon.addPoint(evt.getX(), evt.getY());
                }

                repaint();
            }

        }

        class MouseClickedListener extends MouseAdapter {

            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                super.mouseClicked(evt);

                if (evt.getButton() == java.awt.event.MouseEvent.BUTTON1) {
                    if (model != null) {
                        ProjectionInstance instance = ((TopicProjectionModel) model).getInstanceByPosition(evt.getPoint());

                        if (instance != null) {
                            try {
                                model.setSelectedInstance(instance);
                                model.notifyObservers();

                                if (OpenDialog.checkCorpus((TopicProjectionModel) model, TopicProjectionFrame.this)) {
                                    if (evt.getClickCount() == 1) {
                                        showContent(((TopicProjectionModel) model).getCorpus(),
                                                (TopicProjectionInstance) instance);
                                    } else {
                                        MultipleFileView.getInstance(TopicProjectionFrame.this).display(instance,
                                                ((TopicProjectionModel) model).getCorpus());
                                    }
                                }
                            } catch (IOException ex) {
                                Logger.getLogger(TopicProjectionFrame.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    }
                } else if (evt.getButton() == java.awt.event.MouseEvent.BUTTON3) {
                    cleanSelectedInstances();
                }
            }

            @Override
            public void mousePressed(java.awt.event.MouseEvent evt) {
                super.mousePressed(evt);

                if (evt.getButton() == java.awt.event.MouseEvent.BUTTON1) {
                    if (model != null) {
                        ProjectionInstance instance = ((TopicProjectionModel) model).getInstanceByPosition(evt.getPoint());

                        if (instance != null) {
                            if (moveinstances) {
                                if (model.getSelectedInstances().contains(instance)) {
                                    selinstance = (TopicProjectionInstance) instance;
                                }
                            }
                        } else {
                            selsource = evt.getPoint();
                        }
                    }
                } else if (evt.getButton() == java.awt.event.MouseEvent.BUTTON3) {
                    selpolygon = new java.awt.Polygon();
                    selpolygon.addPoint(evt.getX(), evt.getY());
                }
            }

            @Override
            public void mouseReleased(java.awt.event.MouseEvent evt) {
                super.mouseReleased(evt);

                if (model != null) {
                    if ((selsource != null && seltarget != null) || selpolygon != null) {
                        ArrayList<TopicProjectionInstance> instances = null;

                        if (selpolygon != null) {
                            instances = getSelectedInstances(selpolygon);
                        } else {
                            instances = getSelectedInstances(selsource, seltarget);
                        }

                        if (instances != null) {
                            if (selection != null) {
                                selection.selected(new ArrayList<AbstractInstance>(instances));
                            }
                        }
                    }
                }

                selpolygon = null;
                selinstance = null;
                selsource = null;
                seltarget = null;
            }

        }

