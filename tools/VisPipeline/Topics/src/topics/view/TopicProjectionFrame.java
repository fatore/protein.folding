        public ArrayList<TopicProjectionInstance> getSelectedInstances(Point source, Point target) {
            ArrayList<ProjectionInstance> selinstances = new ArrayList<ProjectionInstance>();

            if (model != null) {
                int x = Math.min(source.x, target.x);
                int width = Math.abs(source.x - target.x);

                int y = Math.min(source.y, target.y);
                int height = Math.abs(source.y - target.y);

                Rectangle rect = new Rectangle(x, y, width, height);
                selinstances = ((TopicProjectionModel) model).getInstancesByPosition(rect);
            }

            return TopicProjectionModel.convertInstances(new ArrayList<AbstractInstance>(selinstances));
        }

